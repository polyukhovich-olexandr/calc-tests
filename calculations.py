# calculations.py


def add(a, b):
    return float(a + b)


def subtract(a, b):
    return float(a - b)


def multiply(a, b):
    return float(a * b)


def divide(a, b):
    return float(a / b)


print(f"2 + 3 = {add(2, 3)}")
print(f"2 - 3 = {subtract(2, 3)}")
print(f"2 * 3 = {multiply(2, 3)}")
print(f"2 / 3 = {divide(2, 3)}")
